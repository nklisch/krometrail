import { describe, expect, it } from "vitest";
import type { CommandsData } from "../../../src/cli/commands/commands.js";
import { generateBashCompletions, generateFishCompletions, generateZshCompletions } from "../../../src/cli/commands/completions.js";

const mockInventory: CommandsData = {
	version: "0.1.0",
	groups: [
		{
			name: "debug",
			description: "Debug commands",
			commands: [
				{
					name: "launch",
					description: "Launch a debug session",
					group: "debug",
					args: [
						{ name: "program", type: "positional", required: true, description: "Program to debug" },
						{ name: "json", type: "boolean", required: false, description: "Output as JSON", default: false },
						{ name: "language", type: "string", required: false, description: "Language" },
					],
				},
				{ name: "stop", description: "Stop a session", group: "debug", args: [] },
			],
		},
		{
			name: "browser",
			description: "Browser commands",
			commands: [{ name: "start", description: "Start recording", group: "browser", args: [] }],
		},
		{
			name: "top-level",
			description: "Top-level commands",
			commands: [
				{ name: "doctor", description: "Check installed debuggers", group: "top-level", args: [] },
				{ name: "commands", description: "List all commands", group: "top-level", args: [] },
			],
		},
	],
};

describe("bash completions", () => {
	it("includes compgen and complete -F _krometrail", () => {
		const output = generateBashCompletions(mockInventory);
		expect(output).toContain("compgen -W");
		expect(output).toContain("complete -F _krometrail krometrail");
	});

	it("includes _krometrail function definition", () => {
		const output = generateBashCompletions(mockInventory);
		expect(output).toContain("_krometrail()");
	});

	it("includes top-level commands", () => {
		const output = generateBashCompletions(mockInventory);
		expect(output).toContain("debug");
		expect(output).toContain("browser");
		expect(output).toContain("doctor");
		expect(output).toContain("commands");
		expect(output).toContain("completions");
	});

	it("includes global flags", () => {
		const output = generateBashCompletions(mockInventory);
		expect(output).toContain("--help");
		expect(output).toContain("--version");
		expect(output).toContain("--mcp");
	});

	it("includes debug subcommands", () => {
		const output = generateBashCompletions(mockInventory);
		expect(output).toContain("launch");
		expect(output).toContain("stop");
	});

	it("includes --flags for subcommands", () => {
		const output = generateBashCompletions(mockInventory);
		expect(output).toContain("--json");
		expect(output).toContain("--language");
	});

	it("includes bash/zsh/fish as completions subcommand options", () => {
		const output = generateBashCompletions(mockInventory);
		expect(output).toContain("bash");
		expect(output).toContain("zsh");
		expect(output).toContain("fish");
	});

	it("includes a header comment with install instructions", () => {
		const output = generateBashCompletions(mockInventory);
		expect(output).toContain("Installation");
		expect(output).toContain("Generated by");
	});
});

