describe("fish completions", () => {
	it("includes complete -c krometrail commands", () => {
		const output = generateFishCompletions(mockInventory);
		expect(output).toContain("complete -c krometrail");
	});

	it("includes __fish_use_subcommand for top-level commands", () => {
		const output = generateFishCompletions(mockInventory);
		expect(output).toContain("__fish_use_subcommand");
	});

	it("includes __fish_seen_subcommand_from for subcommands", () => {
		const output = generateFishCompletions(mockInventory);
		expect(output).toContain("__fish_seen_subcommand_from");
	});

	it("includes top-level commands", () => {
		const output = generateFishCompletions(mockInventory);
		expect(output).toContain("debug");
		expect(output).toContain("browser");
		expect(output).toContain("doctor");
		expect(output).toContain("completions");
	});

	it("includes debug subcommands", () => {
		const output = generateFishCompletions(mockInventory);
		expect(output).toContain("launch");
		expect(output).toContain("stop");
	});

	it("includes -d description for completions", () => {
		const output = generateFishCompletions(mockInventory);
		expect(output).toContain("-d '");
	});

	it("includes flags from inventory", () => {
		const output = generateFishCompletions(mockInventory);
		// fish uses -l 'flagname' syntax rather than --flagname
		expect(output).toContain("-l 'json'");
		expect(output).toContain("-l 'language'");
	});

	it("includes bash/zsh/fish as completions subcommand options", () => {
		const output = generateFishCompletions(mockInventory);
		expect(output).toContain("__fish_seen_subcommand_from completions");
	});

	it("includes a header comment with install instructions", () => {
		const output = generateFishCompletions(mockInventory);
		expect(output).toContain("Installation");
		expect(output).toContain("Generated by");
	});
});
